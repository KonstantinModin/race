const getEmptyRow = (width) => [...Array(width)].fill(0);

const getRowWithObstacle = (width) => {
  const obstacleIdx = (width * Math.random()) | 0;
  const obstacle = ((Math.random() * 6) | 0) + 5;
  return [...Array(width)].map((_, idx) =>
    idx === obstacleIdx ? obstacle : 0
  );
};

const generateLevel = (percentage, width) => {
  return [...Array(50)].map((_, idx) => {
    if (idx | 1 && Math.random() < percentage) {
      return getRowWithObstacle(width);
    }
    return getEmptyRow(width);
  });
};

